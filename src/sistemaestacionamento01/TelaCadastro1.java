    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            con = new ConnectionFactory().getConnection();

            Cliente c = new Cliente();
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

            c.setNomeCliente(jTnomeCompleto.getText());
            c.setCpf(jTcpf.getText());

            c.setDataNascimento((jTDataNascimento.getText()));

            if (jCdeficiencia.getSelectedItem() == "sim"){
                c.setDeficiencia(1);
            } else {
                c.setDeficiencia(0);
            }

            c.setLogin(jTlogin.getText());
            String passString = new String(jPsenha.getPassword());
            c.setSenha(passString);
            c.setEmail(jTemail.getText());
            c.setPlacaCarro(jTplacaCarro.getText());

          

            String sql = "insert into cliente " +
            "(clienteNome, dataNascimento, deficiencia, clienteCpf, clienteEmail, clienteLogin, ClienteSenha, placaCarro) " +
            "values (?,?,?,?,?,?,?,?)";

            PreparedStatement stmt = con.prepareStatement(sql);
            

            stmt.setString(1, c.getNomeCliente());

            stmt.setString(2, c.getDataNascimento());

            stmt.setString(4, c.getCpf());

            if (jCdeficiencia.getSelectedItem() == "sim"){
                stmt.setInt(3, 1);
            } else {
                stmt.setInt(3, 0);
            }
            stmt.setString(8, c.getPlacaCarro());
            stmt.setString(5, c.getEmail());
            stmt.setString(7, c.getSenha());
            stmt.setString(6, c.getLogin());

            // executa

            stmt.execute();
            stmt.close();
            JOptionPane.showMessageDialog(null, "Usuario cadastrado com sucesso");
            con.close();
        } catch (SQLException ex) {
            Logger.getLogger(TelaCadastro1.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

